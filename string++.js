/*Написать функцию, которая принимает любое количество строк, объединяет их в одну длинную строку и
возвращает ее. */

var str0="";
var strn=[];
for(let i=0;strn[i]=confirm("введёте строку?");i++){
    strn[i]=prompt("введите строку: ");
    str0=str0+' '+strn[i];
}
document.write("итоговая строка: ",str0);